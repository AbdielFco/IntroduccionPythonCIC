# Strings
print("Hello world")
print('Hello World')
print('''Hello World''')
print("""Hello World""")

print(type("Hello World"))
print("Bye" + "World")

# Entero
print(30)
print(type(30))

# Flotante
print(30.5)
print(type(30.5))

# Booleano
print(f"{True} \n {False}")
print(type(True))

# Listas
[10,20,30,55]
["hello","bye","arriverdechi"]
[10,"hello",True,10.1]
print(type([1,2]))

# Tuplas
(10,20,30,55)
print(type((1,2)))

# Diccionarios
print(type({
    "name": "Ryan",
    "lastname": "Garcia"    
}))

# Ninguno 
print(type(None))